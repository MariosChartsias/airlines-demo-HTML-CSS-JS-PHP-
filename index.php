		<h2 style="color:white; position:relative; top:-18cm; right:7.5cm;">Definition</h2>
		<p style=" text-align:justify; margin-top:-18.2cm; margin-left:10.7cm; ">A Flight Manual is a manual, associated with the Certificate of Airworthiness, containing limitations within which the aircraft is to be considered airworthy,<br> and instructions and information necessary to the flight crew members for the safe operation of the aircraft. (ICAO Annex 6).</p>
		<h2 style="color:white; position:relative; right:7.3cm;">Description</h2>
		<p style="text-align:justify; position:relative; left:10.7cm;">An aeroplane flight manual (AFM) is a document produced by the aircraft manufacturer containing detailed information on the operation of the aircraft. The AFM <br> details the recommended aircraft operating technique for normal, abnormal and emergency operation together with the Aircraft Performance that should be achieved <br> when the aircraft is operated in accordance with these procedures.
														The AFM is a vital part of the aircraft inventory and must be carried on all flights unless the National <br> Airworthiness Authority (National Aviation Authority (NAA)) of the aircraft operator has formally accepted that the Operations Manual of the aircraft operator replicates all <br> relevant AFM information for an aircraft. The AFM is specific to each aircraft and reflects the precise equipment and modification state of that aircraft. In practice, National<br> Aviation Authority (NAA) dispensation not to carry the AFM on board an aircraft is commonly obtained by commercial aircraft operators.
														Prompt Amendment of the AFM is <br> an important function of an aircraft operator's flight operations department and must be carefully controlled. The control process must also ensure that any documents dependent<br> upon AFM content such as the Operations Manual, flight deck copies of the Emergency and Abnormal Checklist and/or Quick Reference Handbook (QRH)) and crew training<br> materials are also updated following the publication of any amendment.</p>
